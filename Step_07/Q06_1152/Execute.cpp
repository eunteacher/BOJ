/*
문제
영어 대소문자와 공백으로 이루어진 문자열이 주어진다. 이 문자열에는 몇 개의 단어가 있을까? 이를 구하는 프로그램을 작성하시오. 단, 한 단어가 여러 번 등장하면 등장한 횟수만큼 모두 세어야 한다.

입력
첫 줄에 영어 대소문자와 공백으로 이루어진 문자열이 주어진다. 이 문자열의 길이는 1,000,000을 넘지 않는다. 단어는 공백 한 개로 구분되며, 공백이 연속해서 나오는 경우는 없다. 또한 문자열은 공백으로 시작하거나 끝날 수 있다.

출력
첫째 줄에 단어의 개수를 출력한다.
*/
#include <iostream>
#include <string>
using namespace std;

int main()
{
	//string s;
	//getline(cin, s);
	char s[1000002] = { 0, }; // NULL 값과 개행문자가 들어가야되서 +2를 해준다
	cin.getline(s, 1000002); // getline()를 사용하는 이유는 공백을 입력 받기 위해서

	int count = 0; // 단어의 개수
	int i = 0; // 문자열의 인덱스

	while (s[i] != NULL)
	{
		if (((s[i] >= 'A' && s[i] <= 'Z') || (s[i] >= 'a' && s[i] <= 'z')) && s[i + 1] == ' ')
		{
			// 현재 값이 문자이고, 다음 문자가 공백일 경우
			count++;

		}
		else if (((s[i] >= 'A' && s[i] <= 'Z') || (s[i] >= 'a' && s[i] <= 'z')) && s[i + 1] == NULL)
		{
			// 현재 값이 문자이고, 다음 문자가 널값일 경우
			count++;
		}

		i++;
	}

	cout << count;
	return 0;
}